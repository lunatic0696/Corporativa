package Fidelidade;

public class Produto {
	
	public float preco;
	public int precoEmPontos;
	public int pontosPorCompra;

	public float getPreco() {
		return preco;
	}
	public void setPreco(float preco) {
		this.preco = preco;
	}
	public int getPrecoEmPontos() {
		return precoEmPontos;
	}
	public void setPrecoEmPontos(int precoEmPontos) {
		this.precoEmPontos = precoEmPontos;
	}
	public int getPontosPorCompra() {
		return pontosPorCompra;
	}
	public void setPontosPorCompra(int pontosPorCompra) {
		this.pontosPorCompra = pontosPorCompra;
	}
	
}
